package com.dnf.entity;

import java.util.Date;

public class OrderMessage {
	private int id;
	private String orderId;
	private String msgType;
	private String state;
	private String phoneNum;
	private String orderMsg;
	private Date createDate;
	private Date reviewDate;
	private String errorLog;
	private String cityCode;
	private String loanTime;
	private String ext1;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getMsgType() {
		return msgType;
	}
	public void setMsgType(String msgType) {
		this.msgType = msgType;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getPhoneNum() {
		return phoneNum;
	}
	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}
	public String getOrderMsg() {
		return orderMsg;
	}
	public void setOrderMsg(String orderMsg) {
		this.orderMsg = orderMsg;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public Date getReviewDate() {
		return reviewDate;
	}
	public void setReviewDate(Date reviewDate) {
		this.reviewDate = reviewDate;
	}
	public String getErrorLog() {
		return errorLog;
	}
	public void setErrorLog(String errorLog) {
		this.errorLog = errorLog;
	}
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public String getLoanTime() {
		return loanTime;
	}
	public void setLoanTime(String loanTime) {
		this.loanTime = loanTime;
	}
	public String getExt1() {
		return ext1;
	}
	public void setExt1(String ext1) {
		this.ext1 = ext1;
	}
	
	@Override
	public String toString() {
		return "OrderMessage [id=" + id + ", orderId=" + orderId + ", msgType="
				+ msgType + ", state=" + state + ", phoneNum=" + phoneNum
				+ ", orderMsg=" + orderMsg + ", createDate=" + createDate
				+ ", reviewDate=" + reviewDate + ", errorLog=" + errorLog
				+ ", cityCode=" + cityCode + ", loanTime=" + loanTime
				+ ", ext1=" + ext1 + "]";
	}
	
	
}
